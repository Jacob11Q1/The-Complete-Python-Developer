# --------------------------------------
# Augmented Assignment Operators
# --------------------------------------

# Starting value
some_value = 5
print("Initial value:", some_value)

# += adds and assigns
some_value += 2   # same as: some_value = some_value + 2
print("After += 2:", some_value)

# -= subtracts and assigns
some_value -= 2   # same as: some_value = some_value - 2
print("After -= 2:", some_value)

# *= multiplies and assigns
some_value *= 2   # same as: some_value = some_value * 2
print("After *= 2:", some_value)

# /= divides and assigns
some_value /= 3   # same as: some_value = some_value / 3
print("After /= 3:", some_value)

# //= floor divides and assigns
some_value //= 2  # same as: some_value = some_value // 2
print("After //= 2:", some_value)

# %= modulus and assigns
some_value %= 3   # same as: some_value = some_value % 3
print("After %= 3:", some_value)

# **= exponentiation and assigns
some_value **= 4  # same as: some_value = some_value ** 4
print("After **= 4:", some_value)
