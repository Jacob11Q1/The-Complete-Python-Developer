# --- Integers (int) in Python ---
# Integers are whole numbers (positive, negative, or zero).

# Addition (+)
print(2 + 4)          # 6
print(type(2 + 4))    # <class 'int'>

# Subtraction (-)
print(2 - 4)          # -2
print(type(2 - 4))    # <class 'int'>

# Multiplication (*)
print(2 * 4)          # 8
print(type(2 * 4))    # <class 'int'>

# Division (/)
# IMPORTANT: always returns a float, even if result is a whole number
print(8 / 4)          # 2.0
print(type(8 / 4))    # <class 'float'>

# Floor Division (//)
# Gives the integer result (no remainder, rounds down if needed)
print(7 // 2)         # 3
print(type(7 // 2))   # <class 'int'>

# Modulo (%)
# Gives the remainder of division
print(7 % 2)          # 1
print(type(7 % 2))    # <class 'int'>

# Exponent (**)
# Raises a number to the power of another
print(2 ** 3)         # 8 (2 * 2 * 2)
print(type(2 ** 3))   # <class 'int'>
